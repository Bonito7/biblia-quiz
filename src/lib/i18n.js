// Fallback to French for languages without full translation
const defaultLang = "fr";

export function t(lang, key) {
  const langData = translations[lang] || translations[defaultLang];
  return langData[key] || translations[defaultLang][key] || key;
}

export function getLanguage() {
  return localStorage.getItem("bibliaQuizLang") || "fr";
}

export function setLanguage(code) {
  localStorage.setItem("bibliaQuizLang", code);
  const lang = LANGUAGES.find(l => l.code === code);
  document.documentElement.dir = lang?.dir || "ltr";
  document.documentElement.lang = code;
}

export function getDir(lang) {
  const l = LANGUAGES.find(l => l.code === lang);
  return l?.dir || "ltr";
}